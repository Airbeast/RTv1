#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c == '\0')
		return (&(((char *)s)[ft_strlen(s)]));
	else
	{
		while (*s != c)
		{
			if (*s == '\0')
				return (NULL);
			s++;
		}
		return ((char *)s);
	}
}
