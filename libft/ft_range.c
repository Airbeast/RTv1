#include "libft.h"

int	ft_range(int x, int range)
{
	int i;
	int p;

	p = 1;
	i = 0;
	if (range < 0)
		return (0);
	else
	{
		while (i < range)
		{
			p *= x;
			i++;
		}
	}
	return (p);
}
