#include "libft.h"

void	ft_sort(int n, char **s)
{
	char	*t;
	int		i;
	int		sw;

	sw = 1;
	i = 1;
	while (sw == 1)
	{
		sw = 0;
		while (i < n - 1)
		{
			if (ft_strcmp(s[i], s[i + 1]) > 0)
			{
				t = s[i];
				s[i] = s[i + 1];
				s[i + 1] = t;
				i++;
				sw = 1;
			}
			else
				i++;
		}
		i = 0;
	}
}
